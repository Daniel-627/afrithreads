import {TagIcon} from '@sanity/icons' // optional, for UI flair
import {defineField, defineType} from 'sanity'

export const audienceType = defineType({
  name: 'audience',
  title: 'Target Audience',
  type: 'document',
  icon: TagIcon,
  fields: [
    defineField({
      name: 'title',
      title: 'Title',
      type: 'string',
    }),
    defineField({
      name: 'slug',
      title: 'Slug',
      type: 'slug',
      options: {
        source: 'title',
      },
    }),
    defineField({
      name: 'description',
      title: 'Description',
      type: 'text',
    }),
  ],
  preview: {
    select: {
      title: 'title',
    },
  },
})
